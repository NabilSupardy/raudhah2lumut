    <h1>Selamat Datang di Pondok Pesantren Ar Raudlatul Hasanah 2 Lumut</h1>
    <p>Pesantren Ar-Raudlatul Hasanah 2 Lumut adalah salah satu lembaga pendidikan Islam berbasis pesantren yang berlokasi di Lumut, Kabupaten Tapanuli Tengah, Sumatra Utara, Indonesia. Pesantren ini merupakan cabang dari Pesantren Ar-Raudlatul Hasanah yang berpusat di Medan.</p>

    <h2>Sejarah Singkat</h2>
    <p>Pesantren Ar-Raudlatul Hasanah merupakan lembaga pendidikan wakaf yang didirikan pada tahun 1982 dan telah resmi dicatat dalam akte notaris M. Djaidir, SH No. 29 tahun 1986 di Medan, dan mulai dibuka program Kulliyatul Mu’allimin Al-Islamiyah (Pendidikan Formal Pesantren) sejak tahun 1986.

Diantara usaha-usaha yang dijalankan oleh Badan Wakaf Pesantren Ar-Rudlatul Hasanah sebagaimana yang tertuang dalam AD/ART Badan Wakaf adalah menerima dan mengurus harta wakaf serta mengelola khazanah dan menyuburkannya.

Pengelolaan harta wakaf serta sumbangan-sumbangan secara baik dan profesional oleh Pesantren Ar-Raudlatul Hasanah semakin mendapatkan kepercayaan dan apresiasi yang tinggi di tengah-tengah masyarakat.

Pada Maret 2011, Pesantren menyambut niat baik dari keluarga Bapak Drs. H. Aman Nasution yang mewakafkan sebidah tanah untuk dijadikan sebuah lembaga Pendidikan Islam yang dikelola langsung oleh Ar-Raudlatul Hasanah yang berlokasi di Lumut – Tapanuli Tengah. Tanah wakaf ini berlokasi di Jl. Lintas Sibolga – Padang Sidempuan, Kec. Lumut, Kab. Tapanuli Tengah dengan luas 1,5 ha. Lokasi tersebut cukup strategis untuk area pendidikan karena dekat dengan jalan raya dan juga berada di dataran tinggi.

Peletakan batu pertama pembangunan dilaksanakan pada tanggal 28 Mei 2011 oleh Bapak Mayor Jenderal H. A. Y. Nasution, Asisten Teritorial TNI (saat itu) dan didampingi oleh pihak Pesantren Ar-Raudlatul Hasanah.

Dan keberadaan Pesantren Ar-Raudlatul Hasanah 2, diharapkan mampu memberikan warna baru dalam dunia pendidikan Indonesia yang berbasis pendidikan akhlak, adab, budi pekerti serta pengajaran ilmu pengetahuan agama dan umum.
    </p>

